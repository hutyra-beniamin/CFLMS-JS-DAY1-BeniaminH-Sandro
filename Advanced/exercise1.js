
// ADDING VARIABLES AND CONVERTING STRING TO NUMBER

var a = 7;
var b = 14;
var c = "21";
var d = '36';
var e = 42;

var c = Number(c);
var d = Number(d);

var sum = a + b + c + d + e;


// MULTIPLYING VARIABLES + CONVERT

var f = '1';
var g = 15;
var h = 8;
var i = "1";

var f = Number(f);
var i = Number(i);

var res = f * g * h * i;


// FINAL RESULT / DIVISION

document.write(sum / res);  // = 1
